=head3 reconstitute_tsv

	create an array of hash refs from a header array and a string of tab-separated data

@param  $args  Input array, containing
			headers => [ @header_array ],  # keys for the hashes
			str     => $string_with_all_values

@output $arrayref of hashes of data

=cut

sub reconstitute_tsv {
	my $args = shift;

	my @arr = map {
		my %hash; @hash{ @{ $args->{headers} } } = split "\t", $_; \%hash;
	} split "\n", $args->{str};

	return [ @arr ];

}

