around 'run_query' => sub {
	my $orig = shift;
	my $self = shift;
	my $args = shift;
	my $ids = $args->{gene_oid};

	warn 'IDs: ' . Dumper $ids;

	if ( $ids ) {
		my $gp_arr = get_gene_scaffold_data();

		my $data;

		for ( @$gp_arr ) {
			$data->{ $_->{gene_oid} } = $_;
		}

		return [ grep { defined $_ } map { $data->{$_} } @$ids ];
	}

	return get_gene_scaffold_data();
};

